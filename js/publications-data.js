function getPublicationsSorted() {
  return [...PUBLICATIONS_DATA];
}

function getPublicationYears() {
  return [...new Set(PUBLICATIONS_DATA.map(p => p.year))].sort((a, b) => b - a);
}

function getPublicationsByYear(year) {
  if (!year || year === 'all') return getPublicationsSorted();
  if (year === 'before2007') return getPublicationsSorted().filter(p => p.year <= 2007);
  return getPublicationsSorted().filter(p => p.year === Number(year));
}

function getJournalShort(journal) {
  const map = {
    'Environmental Science and Technology': 'EST',
    'Environmental Science & Technology': 'EST',
    'Water Research': 'WR',
    'Nature Communications': 'Nat. Commun.',
    'Science Bulletin': 'Sci. Bull.',
    'Journal of Hazardous Materials': 'J. Hazard. Mater.',
    'Environmental Pollution': 'Environ. Pollut.',
    'Science of the Total Environment': 'Sci. Total Environ.',
    'Chemical Engineering Journal': 'Chem. Eng. J.',
    'Bioresource Technology': 'Bioresour. Technol.',
    'Applied Catalysis B-Environmental': 'Appl. Catal. B',
    'Chemosphere': 'Chemosphere',
    'Groundwater': 'Groundwater',
    'ACS Sustainable Chemistry and Engineering': 'ACS Sustain. Chem. Eng.',
    'Journal of Colloid and Interface Science': 'J. Colloid Interface Sci.',
    'Journal of Environmental Quality': 'J. Environ. Qual.',
  };
  for (const [key, val] of Object.entries(map)) {
    if (journal.startsWith(key)) return val;
  }
  const first = journal.split(/[.,\d]/)[0].trim();
  return first.length > 30 ? first.slice(0, 28) + '…' : first;
}
