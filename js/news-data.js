function getNewsById(id) {
  return NEWS_DATA.find(n => n.id === id);
}

function getNewsSorted() {
  return [...NEWS_DATA].sort((a, b) => new Date(b.date) - new Date(a.date));
}

function getNewsByCategory(category) {
  if (!category || category === 'all') return getNewsSorted();
  return getNewsSorted().filter(n => n.category === category);
}

function formatNewsDate(news) {
  const d = new Date(news.date);
  const day = String(d.getDate()).padStart(2, '0');
  const month = `${d.getFullYear()}.${String(d.getMonth() + 1).padStart(2, '0')}`;
  return { day, month, full: news.date };
}

function getCategoryClass(category) {
  const map = {
    '论文': 'tag-paper',
    '学术会议': 'tag-conference',
    '获奖': 'tag-award',
    '组会通知': 'tag-notice',
    '成果发布': 'tag-release'
  };
  return map[category] || 'tag-default';
}
